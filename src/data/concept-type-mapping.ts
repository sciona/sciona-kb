/**
 * Map a raw concept_type to its canonical broad type.
 * Returns the input unchanged if it's already canonical.
 */
export function canonicalizeConcept(raw: string): string {
  const lower = raw.toLowerCase();
  if (CANONICAL_TYPES.has(lower)) return lower;
  return MAPPING[lower] ?? "custom";
}
